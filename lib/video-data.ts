export function getVideosByCategory(category: string, subcategory?: string): Video[] {
  return videoDatabase.filter((video) => {
    const categoryMatch = video.category === category
    if (subcategory) {
      return categoryMatch && video.subcategory === subcategory
    }
    return categoryMatch
  })
}

export function getFeaturedVideos(count = 9): Video[] {
  // Return a mix of highly rated videos from different categories
  const featured = videoDatabase
    .filter((video) => video.rating >= 4.7)
    .sort(
      (a, b) =>
        Number.parseFloat(b.views.replace("K", "").replace("M", "000")) -
        Number.parseFloat(a.views.replace("K", "").replace("M", "000")),
    )
    .slice(0, count)

  return featured
}

export function searchVideos(query: string): Video[] {
  const lowercaseQuery = query.toLowerCase()
  return videoDatabase.filter(
    (video) =>
      video.title.toLowerCase().includes(lowercaseQuery) ||
      video.description.toLowerCase().includes(lowercaseQuery) ||
      video.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery)) ||
      video.category.toLowerCase().includes(lowercaseQuery) ||
      video.instructor.toLowerCase().includes(lowercaseQuery),
  )
}

export function getCategoryCount(category: string): number {
  return videoDatabase.filter((video) => video.category === category).length
}
